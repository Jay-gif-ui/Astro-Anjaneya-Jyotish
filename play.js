document.addEventListener("DOMContentLoaded", () => {
  const modal = document.getElementById("bookingModal");
  const closeBtn = document.getElementById("closeBooking");
  const payBtn = document.getElementById("payNowBtn");
  const serviceSelect = document.getElementById("serviceSelect");
  const priceBox = document.getElementById("priceBox");
  const languageSwitcher = document.getElementById("languageSwitcher");
  const hamburger = document.getElementById("hamburger");
  const navLinks = document.getElementById("navLinks");
  const bookingDate = document.getElementById("bookingDate");
  const scrollTopBtn = document.querySelector(".left-btn");
  const floatingButtons = document.querySelectorAll(".floating-btn");
  let currentLanguage = "hi";

  const openButtons = [
    document.getElementById("openBooking"),
    document.getElementById("openBooking2"),
    document.getElementById("openKundli")
  ].filter(Boolean);

  const serviceLabels = {
    en: {
      "": "Select Consultation",
      "Kundali Reading & Analysis": "Kundali Reading & Analysis - ₹2500",
      "Daily Horoscope": "Daily Horoscope - ₹99",
      "Compatibility Analysis": "Compatibility Analysis - ₹299",
      "Career Counseling": "Career Counseling - ₹499"
    },
    hi: {
      "": "परामर्श चुनें",
      "Kundali Reading & Analysis": "कुंडली रीडिंग एवं विश्लेषण - ₹2500",
      "Daily Horoscope": "दैनिक राशिफल - ₹99",
      "Compatibility Analysis": "अनुकूलता विश्लेषण - ₹299",
      "Career Counseling": "करियर परामर्श - ₹499"
    }
  };

  if (bookingDate) {
    bookingDate.min = new Date().toISOString().split("T")[0];
  }

  const updatePrice = () => {
    if (!serviceSelect || !priceBox) return;
    const selectedOption = serviceSelect.options[serviceSelect.selectedIndex];
    const price = selectedOption?.getAttribute("data-price") || "0";
   priceBox.textContent = `${translations[currentLanguage].pricePrefix}: ₹${price}`;
  };

  const applyLanguage = (lang) => {
    currentLanguage = translations[lang] ? lang : "en";
    const dictionary = translations[currentLanguage];
    document.documentElement.lang = currentLanguage;
    localStorage.setItem("siteLanguage", currentLanguage);

    if (languageSwitcher) languageSwitcher.value = currentLanguage;

    document.querySelectorAll("[data-translate]").forEach((element) => {
      const key = element.getAttribute("data-translate");
      if (!dictionary[key]) return;
      element.innerHTML = dictionary[key];
    });

    document.querySelectorAll(".service-card button[data-service]").forEach((button) => {
      button.textContent = dictionary.learnMore;
    });

    const nameInput = document.getElementById("userName");
    const emailInput = document.getElementById("userEmail");
    if (nameInput) nameInput.placeholder = dictionary.namePlaceholder;
    if (emailInput) emailInput.placeholder = dictionary.emailPlaceholder;
    if (payBtn) payBtn.textContent = dictionary.payNow;

    if (serviceSelect) {
      Array.from(serviceSelect.options).forEach((option) => {
        option.textContent = serviceLabels[currentLanguage][option.value] || option.textContent;
      });
    }

    const bookingTime = document.getElementById("bookingTime");
    if (bookingTime?.options[0]) bookingTime.options[0].textContent = dictionary.selectTime;

    updatePrice();
  };

  const openModal = (serviceName = "") => {
    if (!modal) return;

    modal.style.display = "flex";
    document.body.classList.add("modal-open");

    if (serviceName && serviceSelect) {
      const option = Array.from(serviceSelect.options).find((item) => item.value === serviceName);
      if (option) {
        serviceSelect.value = serviceName;
        updatePrice();
      }
    }

    document.getElementById("userName")?.focus();
  };

  const closeModal = () => {
    if (!modal) return;
    modal.style.display = "none";
    document.body.classList.remove("modal-open");
  };

  openButtons.forEach((button) => {
    button.addEventListener("click", () => openModal(button.id === "openKundli" ? "Kundali Reading & Analysis" : ""));
  });

  document.querySelectorAll(".service-card button[data-service]").forEach((button) => {
    button.addEventListener("click", () => openModal(button.dataset.service));
  });

  closeBtn?.addEventListener("click", closeModal);

  modal?.addEventListener("click", (event) => {
    if (event.target === modal) closeModal();
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeModal();
  });

  serviceSelect?.addEventListener("change", updatePrice);

  payBtn?.addEventListener("click", () => {
    const name = document.getElementById("userName")?.value.trim();
    const email = document.getElementById("userEmail")?.value.trim();
    const date = document.getElementById("bookingDate")?.value;
    const time = document.getElementById("bookingTime")?.value;
    const selectedOption = serviceSelect?.options[serviceSelect.selectedIndex];
    const service = selectedOption?.value;
    const price = selectedOption?.getAttribute("data-price") || "0";
    const paymentLink = selectedOption?.getAttribute("data-link");

    if (!name || !email || !date || !time || !service) {
      alert(translations[currentLanguage].fillDetails);
      return;
    }

    localStorage.setItem("bookingName", name);
    localStorage.setItem("bookingEmail", email);
    localStorage.setItem("bookingDate", date);
    localStorage.setItem("bookingTime", time);
    localStorage.setItem("bookingService", service);
    localStorage.setItem("bookingAmount", price);

    if (!paymentLink) {
      alert("Payment link is not available for this service yet.");
      return;
    }

    window.location.href = paymentLink;
  });

  languageSwitcher?.addEventListener("change", (event) => {
    applyLanguage(event.target.value);
  });

  hamburger?.addEventListener("click", () => {
    const isOpen = navLinks?.classList.toggle("active");
    hamburger.setAttribute("aria-expanded", String(Boolean(isOpen)));
  });

  navLinks?.querySelectorAll("a, button").forEach((item) => {
    item.addEventListener("click", () => {
      navLinks.classList.remove("active");
      hamburger?.setAttribute("aria-expanded", "false");
    });
  });

  scrollTopBtn?.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });

  const toggleFloatingButtons = () => {
    const shouldShow = window.scrollY > window.innerHeight * 0.55;
    floatingButtons.forEach((button) => button.classList.toggle("is-visible", shouldShow));
  };

  window.addEventListener("scroll", toggleFloatingButtons, { passive: true });
  toggleFloatingButtons();

  document.querySelectorAll(".bg-star").forEach((star) => {
    setInterval(() => {
      const randomX = Math.random() * 10 - 5;
      const randomY = Math.random() * 10 - 5;
      star.style.setProperty("--drift-x", `${randomX}px`);
      star.style.setProperty("--drift-y", `${randomY}px`);
    }, 3000);
  });

  applyLanguage(currentLanguage);
});

